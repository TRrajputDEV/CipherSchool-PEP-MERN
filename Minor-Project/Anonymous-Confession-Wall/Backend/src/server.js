import express from "express";
import mongoose from "mongoose";
import session from "express-session";
import passport from "passport";
import cors from "cors";
import dotenv from "dotenv";

// Load env variables first
dotenv.config();

// Passport config (must import after dotenv)
import "./config/passport.js";

// Routes
import authRoutes from "./routes/auth.routes.js";
import confessionRoutes from "./routes/confession.routes.js";

const app = express();

// â”€â”€ Middleware â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use(
  cors({
    origin: process.env.CLIENT_URL,
    credentials: true, // allow cookies from frontend
  })
);

app.use(express.json());

app.use(
  session({
    secret: process.env.SESSION_SECRET,
    resave: false,
    saveUninitialized: false,
  })
);

app.use(passport.initialize());
app.use(passport.session());

// â”€â”€ API Routes â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use("/api/auth", authRoutes);
app.use("/api/confessions", confessionRoutes);

// â”€â”€ Connect DB then Start Server â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
mongoose
  .connect(process.env.MONGO_URI)
  .then(() => {
    console.log("âœ… MongoDB connected");
    app.listen(process.env.PORT, () =>
      console.log(`ğŸš€ Server running â†’ http://localhost:${process.env.PORT}`)
    );
  })
  .catch((err) => {
    console.error("âŒ DB connection failed:", err.message);
    process.exit(1);
  });